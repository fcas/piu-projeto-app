    public void listar(){
        lista.clear();
        if (Informacoes.pratos!=null){
        for(int i = 0 ; i < Informacoes.pratos.size() ; i++){
            if((!cb1.isSelected()&&!cb2.isSelected()&&!cb3.isSelected()&&!cb4.isSelected())||(("brasileira".equals(Informacoes.pratos.get(i).culinaria)&&cb1.isSelected())||("chinesa".equals(Informacoes.pratos.get(i).culinaria)&&cb2.isSelected())||("italiana".equals(Informacoes.pratos.get(i).culinaria)&&cb3.isSelected())||("japonesa".equals(Informacoes.pratos.get(i).culinaria)&&cb4.isSelected()))){
                lista.add(Informacoes.pratos.get(i));
        }
        }
        }
        
    }
    
    public void mostrar(){
        listar();
    while(tipo.getRowCount()>0){  
        tipo.removeRow(0);  
    }
    if (lista.isEmpty()){
    }
    else{

        String[] linha = new String[] {null, null};
        for(int j = 0; j < lista.size(); j++){
            tipo.addRow(linha);
            tipo.setValueAt(lista.get(j).nome, j, 0);
            tipo.setValueAt(lista.get(j).culinaria, j, 1);
            
        }
    }
    }
